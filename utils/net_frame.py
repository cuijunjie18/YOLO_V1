def predict_ch8(prefix, num_preds, net, vocab, device):  #@save
    """在prefix后面生成新字符"""
    state = net.begin_state(batch_size=1, device=device)
    outputs = [vocab[prefix[0]]]
    # print(len(outputs),outputs[0])
    get_input = lambda: torch.tensor([outputs[-1]], device=device).reshape((1, 1))
    # print(get_input().shape)
    for y in prefix[1:]:  # 预热期
        _, state = net(get_input(), state)
        outputs.append(vocab[y])
    # print(outputs)
    # print([vocab.idx_to_token[i] for i in outputs])
    for _ in range(num_preds):  # 预测num_preds步
        y, state = net(get_input(), state)
        # print(y.shape)
        outputs.append(int(y.argmax(dim=1).reshape(1)))
    return ''.join([vocab.idx_to_token[i] for i in outputs])

def grad_clipping(net, theta):  #@save
    """裁剪梯度"""
    if isinstance(net, nn.Module):
        params = [p for p in net.parameters() if p.requires_grad]
    else:
        params = net.params
    norm = torch.sqrt(sum(torch.sum((p.grad ** 2)) for p in params))    # 求梯度的范数
    if norm > theta:
        for param in params:
            param.grad[:] *= theta / norm

class RNNModel(nn.Module): #@save
    """循环神经网络模型"""
    def __init__(self, rnn_layer, vocab_size, **kwargs):
        super(RNNModel, self).__init__(**kwargs)
        self.rnn = rnn_layer
        self.vocab_size = vocab_size
        self.num_hiddens = self.rnn.hidden_size
        # 如果RNN是双向的（之后将介绍），num_directions应该是2，否则应该是1
        if not self.rnn.bidirectional:
            self.num_directions = 1
            self.linear = nn.Linear(self.num_hiddens, self.vocab_size)
        else:
            self.num_directions = 2
            self.linear = nn.Linear(self.num_hiddens * 2, self.vocab_size)

    def forward(self, inputs, state):
        """
        inputs : shape = (批大小，时间步)
        """
        X = F.one_hot(inputs.T.long(), self.vocab_size)
        X = X.to(torch.float32)
        Y, state = self.rnn(X, state)
        # print(f"Y_shape : {Y.shape}")
        # 全连接层首先将Y的形状改为(时间步数*批量大小,隐藏单元数)
        # 它的输出形状是(时间步数*批量大小,词表大小)。
        output = self.linear(Y.reshape((-1, Y.shape[-1])))
        # print(f"Output shape : {output.shape}")
        return output, state

    def begin_state(self, device, batch_size=1):
        if not isinstance(self.rnn, nn.LSTM):
            # nn.GRU以张量作为隐状态
            return  torch.zeros((self.num_directions * self.rnn.num_layers,
                                 batch_size, self.num_hiddens),
                                device=device)
        else:
            # nn.LSTM以元组作为隐状态
            return (torch.zeros((
                self.num_directions * self.rnn.num_layers,
                batch_size, self.num_hiddens), device=device),
                    torch.zeros((
                        self.num_directions * self.rnn.num_layers,
                        batch_size, self.num_hiddens), device=device))


# 二、用于机器翻译的函数
def read_data_nmt(data_path): #@save
    """读入原始的数据"""
    with open(data_path,'r',encoding = 'utf-8') as file:
        return file.read()

def preprocess_nmt(text): #@save
    """预处理“英语－中文”数据集"""
    def no_space(char, prev_char):
        """仅判断英文字符"""
        return char in set(',.!?') and prev_char != ' '

    # 使用空格替换不间断空格
    # 使用小写字母替换大写字母
    text = text.replace('\u202f', ' ').replace('\xa0', ' ').lower()
    # 在单词和标点符号之间插入空格
    out = [' ' + char if i > 0 and no_space(char, text[i - 1]) else char
           for i, char in enumerate(text)]
    return ''.join(out)

def tokenize_nmt(text,num_examples=None,to_lan = "Chinese"): #@save
    """词元化“英语-法语”数据集"""
    source, target = [], []
    for i, line in enumerate(text.split('\n')):
        if num_examples and i > num_examples:
            break
        parts = line.split('\t')
        if len(parts) == 2:
            source.append(parts[0].split(' ')) # 英文tokenize以' '切分
            if to_lan == None:
                target.append(parts[1].split(' ')) # 目标语种为英文俺' '划分
            else:
                target.append(list(parts[1])) # 中文直接按字分
    return source, target

def truncate_pad(line, num_steps, padding_token): #@save
    """截断或填充文本序列"""
    if len(line) > num_steps:
        return line[:num_steps]  # 截断
    return line + [padding_token] * (num_steps - len(line))  # 填充

def build_array_nmt(lines, vocab, num_steps): #@save
    """将机器翻译的文本序列转换成小批量"""
    lines = [vocab[l] for l in lines]
    lines = [l + [vocab['<eos>']] for l in lines]
    array = torch.tensor([truncate_pad(
        l, num_steps, vocab['<pad>']) for l in lines])
    valid_len = (array != vocab['<pad>']).type(torch.int32).sum(1)
    return array, valid_len

def load_data_nmt(batch_size, num_steps, data_path = 'en-fn/fra.txt',num_examples=600): #@save
    """返回翻译数据集的迭代器和词表"""
    text = preprocess_nmt(read_data_nmt(data_path))
    source, target = tokenize_nmt(text,num_examples,to_lan = 'Chinese')
    # breakpoint()
    src_vocab = Vocab(source,reserved_tokens=['<pad>', '<bos>', '<eos>'])
    tgt_vocab = Vocab(target,reserved_tokens=['<pad>', '<bos>', '<eos>'])
    src_array, src_valid_len = build_array_nmt(source, src_vocab, num_steps)
    tgt_array, tgt_valid_len = build_array_nmt(target, tgt_vocab, num_steps)
    data_arrays = (src_array, src_valid_len, tgt_array, tgt_valid_len)
    data_iter = load_array(data_arrays, batch_size)
    return data_iter, src_vocab, tgt_vocab

class Encoder(nn.Module): #@save
    """编码器-解码器架构的基本编码器接口"""
    def __init__(self, **kwargs):
        super(Encoder, self).__init__(**kwargs)

    def forward(self, X, *args):
        raise NotImplementedError
    
class Decoder(nn.Module): #@save
    """编码器-解码器架构的基本解码器接口"""
    def __init__(self, **kwargs):
        super(Decoder, self).__init__(**kwargs)

    def init_state(self, enc_outputs, *args):
        raise NotImplementedError

    def forward(self, X, state):
        raise NotImplementedError

class EncoderDecoder(nn.Module): #@save
    """编码器-解码器架构的基类"""
    def __init__(self, encoder, decoder, **kwargs):
        super(EncoderDecoder, self).__init__(**kwargs)
        self.encoder = encoder
        self.decoder = decoder

    def forward(self, enc_X, dec_X, *args):
        enc_outputs = self.encoder(enc_X, *args)
        dec_state = self.decoder.init_state(enc_outputs, *args)
        return self.decoder(dec_X, dec_state)

class Seq2SeqEncoder(Encoder): #@save
    """用于序列到序列学习的循环神经网络编码器"""
    def __init__(self,vocab_size,embed_size,num_hiddens,num_layers,
                 dropout = 0,**kwargs):
        super(Seq2SeqEncoder,self).__init__(**kwargs)
        # 定义嵌入层
        self.embedding = nn.Embedding(vocab_size,embed_size) # 取代one_hot,vocab_size即输入X的范围
        self.rnn = nn.GRU(embed_size,num_hiddens,num_layers,dropout = dropout)

    def forward(self,X:torch.Tensor,*args):
        # 输入X.shape = (batch_size，num_steps)
        # 嵌入层后X.shape = (batch_size，num_steps,embed_size)  
        # X = X.to(torch.int32) # debug用
        X = self.embedding(X)

        # 时间步放第一维，即rnn的输入格式
        X = X.permute(1,0,2)

        # 执行编码
        # output.shape = (num_steps,batch_size,num_hiddens)
        # state.shape = (num_layers,batch_size,num_hiddens)，隐状态与时间步无关
        output,state = self.rnn(X)
        return output,state

class Seq2SeqDecoder(Decoder): #@save
    """用于序列到序列学习的循环神经网络解码器"""
    def __init__(self,vocab_size,embed_size,num_hiddens,num_layers,
                 dropout = 0,**kargs):
        super(Seq2SeqDecoder,self).__init__(**kargs)
        self.embedding = nn.Embedding(vocab_size,embed_size)

        # 这个rnn层的输入维度为(词嵌入+上下文向量).size
        self.rnn = nn.GRU(embed_size + num_hiddens,num_hiddens,num_layers,dropout = dropout) 
        self.dense = nn.Linear(num_hiddens,vocab_size)

    def init_state(self, enc_outputs, *args):
        return enc_outputs[1] #返回编码器的状态

    def forward(self,X : torch.Tensor,state : torch.Tensor):
        # 输入X.shape = (batch_size，num_steps)
        # 嵌入层后X.shape = (batch_size，num_steps,embed_size)
        # print(f"Decoder input shape:{X.shape}")
        X = self.embedding(X).permute(1,0,2)

        # 取编码器最后一层的state为初始状态，同时广播context，使其具有与X相同的num_steps
        context = state[-1].repeat(X.shape[0],1,1)

        # 拼接上下文,shape = (num_steps,batch_size,num_hiddens + embed_size)
        X_and_context = torch.cat((X,context),dim = 2) 
        output,state = self.rnn(X_and_context,state)
        output = self.dense(output).permute(1,0,2) # shape = (batch_size,num_steps,vocab_size)
        return output,state

def sequence_mask(X, valid_len, value=0): #@save
    """在序列中屏蔽不相关的项"""
    maxlen = X.size(1)
    mask = torch.arange((maxlen), dtype=torch.float32,
                        device=X.device)[None, :] < valid_len[:, None]
    # print(mask)
    X[~mask] = value
    return X

class MaskedSoftmaxCELoss(nn.CrossEntropyLoss): #@save
    """带遮蔽的softmax交叉熵损失函数"""
    # pred的形状：(batch_size,num_steps,vocab_size)
    # label的形状：(batch_size,num_steps)
    # valid_len的形状：(batch_size,)
    def forward(self, pred, label, valid_len):
        weights = torch.ones_like(label)
        weights = sequence_mask(weights, valid_len)
        self.reduction='none'
        unweighted_loss = super(MaskedSoftmaxCELoss, self).forward(
            pred.permute(0, 2, 1), label)
        weighted_loss = (unweighted_loss * weights).mean(dim=1)
        return weighted_loss
    
def predict_seq2seq(net, src_sentence, src_vocab, tgt_vocab, num_steps,
                    device, token = 'word',save_attention_weights=False): #@save
    """序列到序列模型的预测"""
    # 在预测时将net设置为评估模式
    net.eval()
    if token == 'word':
        src_tokens = src_vocab[src_sentence.lower().split(' ')] + [src_vocab['<eos>']] # 单词级别token
    else:
        src_tokens = src_vocab[list(src_sentence)] + [src_vocab['<eos>']]
    # print(src_vocab.to_tokens(src_tokens))
    enc_valid_len = torch.tensor([len(src_tokens)], device=device)
    src_tokens = d2l.truncate_pad(src_tokens, num_steps, src_vocab['<pad>'])

    # 添加批量轴，即让batch_size = 1
    enc_X = torch.unsqueeze(
        torch.tensor(src_tokens, dtype=torch.long, device=device), dim=0)
    # print(enc_X.shape)
    # return
    enc_outputs = net.encoder(enc_X, enc_valid_len)
    dec_state = net.decoder.init_state(enc_outputs, enc_valid_len)
    # 添加批量轴
    dec_X = torch.unsqueeze(torch.tensor(
        [tgt_vocab['<bos>']], dtype=torch.long, device=device), dim=0)
    output_seq, attention_weight_seq = [], []
    for _ in range(num_steps):
        Y, dec_state = net.decoder(dec_X, dec_state)
        # 我们使用具有预测最高可能性的词元，作为解码器在下一时间步的输入
        dec_X = Y.argmax(dim=2)
        pred = dec_X.squeeze(dim=0).type(torch.int32).item()
        # 保存注意力权重（稍后讨论）
        if save_attention_weights:
            attention_weight_seq.append(net.decoder.attention_weights)
        # 一旦序列结束词元被预测，输出序列的生成就完成了
        if pred == tgt_vocab['<eos>']:
            break
        output_seq.append(pred)
    return ' '.join(tgt_vocab.to_tokens(output_seq)), attention_weight_seq

def bleu(pred_seq, label_seq, k,token = 'word'):  #@save
    """计算BLEU"""
    pred_tokens = pred_seq.split(' ')
    if token == 'word':
        label_tokens = label_seq.split(' ')
    else:
        label_tokens = list(label_seq)
    len_pred, len_label = len(pred_tokens), len(label_tokens)
    score = math.exp(min(0, 1 - len_label / len_pred))
    for n in range(1, k + 1):
        num_matches, label_subs = 0, collections.defaultdict(int)
        for i in range(len_label - n + 1):
            label_subs[' '.join(label_tokens[i: i + n])] += 1
        for i in range(len_pred - n + 1):
            if label_subs[' '.join(pred_tokens[i: i + n])] > 0:
                num_matches += 1
                label_subs[' '.join(pred_tokens[i: i + n])] -= 1
        score *= math.pow(num_matches / (len_pred - n + 1), math.pow(0.5, n))
    return score


